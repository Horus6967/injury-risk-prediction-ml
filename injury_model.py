import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import (
    precision_recall_curve,
    confusion_matrix,
    ConfusionMatrixDisplay,
    classification_report,
    roc_auc_score
)

from sklearn.ensemble import RandomForestClassifier
from sklearn.linear_model import LogisticRegression

from imblearn.over_sampling import SMOTE
import shap


# 1 LOAD DATA


df = pd.read_csv("week_approach_maskedID_timeseries.csv")

print("Dataset shape:", df.shape)


# 2 PREPARE FEATURES


X = df.drop(columns=["injury"])
y = df["injury"]

# scale features
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)


# 3 TRAIN TEST SPLIT


X_train, X_test, y_train, y_test = train_test_split(
    X_scaled,
    y,
    test_size=0.2,
    random_state=42
)


# 4 HANDLE CLASS IMBALANCE


smote = SMOTE(random_state=42)

X_train_bal, y_train_bal = smote.fit_resample(X_train, y_train)

print("After SMOTE:", np.bincount(y_train_bal))


# 5 TRAIN RANDOM FOREST


model = RandomForestClassifier(
    n_estimators=300,
    max_depth=12,
    class_weight="balanced",
    random_state=42
)

model.fit(X_train_bal, y_train_bal)

print("Model trained")


# 6 PREDICT PROBABILITIES


probs = model.predict_proba(X_test)[:,1]

rf_auc = roc_auc_score(y_test, probs)

print("Random Forest AUC:", rf_auc)


# 7 FIND BEST THRESHOLD


precision, recall, thresholds = precision_recall_curve(y_test, probs)

f1_scores = 2*(precision*recall)/(precision+recall+1e-9)

best_index = np.argmax(f1_scores)

best_threshold = thresholds[best_index]

print("Best threshold:", best_threshold)
print("Precision:", precision[best_index])
print("Recall:", recall[best_index])

preds = (probs > best_threshold).astype(int)


# 8 CONFUSION MATRIX


cm = confusion_matrix(y_test, preds)

disp = ConfusionMatrixDisplay(confusion_matrix=cm)

disp.plot(cmap="Blues")

plt.title("Confusion Matrix")
plt.show()

print(classification_report(y_test, preds))


# 9 BASELINE MODEL


baseline = LogisticRegression(max_iter=1000, class_weight="balanced")

baseline.fit(X_train_bal, y_train_bal)

baseline_probs = baseline.predict_proba(X_test)[:,1]

baseline_auc = roc_auc_score(y_test, baseline_probs)

print("Baseline Logistic Regression AUC:", baseline_auc)


# 10 SHAP EXPLANATION


X_test_df = pd.DataFrame(X_test, columns=X.columns)

sample = X_test_df.sample(200, random_state=42)

explainer = shap.TreeExplainer(model)

shap_values = explainer(sample)

